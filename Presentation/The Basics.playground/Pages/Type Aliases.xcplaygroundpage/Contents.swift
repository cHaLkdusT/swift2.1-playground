//: # Type Aliases
/*:
_Type aliases_ define an alternative name for an existing type. You defined type aliases with the `typealias` keyword.

Type aliases are useful when you want to refer to an existing type by a name that is contextually more appropriate, such as when working with the date of a specific size from external source.
*/
