 ## Global and Local Variables
 The capabilities describe above for computing and observing properties are also available to _global variables_ and _local variables_. Global variables are variables that are defined outside of any function, method, closure or type context. Local variable are variables that are defined within a function, method, or closure context.
 
 The global and local variables you have encountered in previous chapters have all been _stored variables_. Stored variables, like stored properties, provide storage for a value of a certain type and allow that value to be set and retrieved.
 
 However, you can also define _computed variables_ and define observers for stored variables, in either a global or local scope. Computed variables calculate their value, rather than storing it, and they are written in the same way as computed properties.
 
 > Global constants and variable are always computed lazily, in a similar manner to Lazy Stored Properties. Unlike lazy stored properties, global constants and variables do not need to be marked with the `lazy` modifier.
