//: ### Unary Minus Operator
//: The sign of numeric value can be toggled using a prefixed `-`.
let three = 3
let minusThree = -three // minus three
let plusThree = -minusThree // plus three
//: It is prepended directly before the value it operates on, without any white space.

//: ### Unary Plus Operator
//: Simply returns the value it operates on, without any changes.
var minusSix = -6
let alsoMinusSix = +minusSix
//: Although the unary plus operator doesn't actually do anything, you can use it to provide symmetry in your code for positive numbers when also using the unary minus operator for negative numbers

